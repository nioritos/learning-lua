--operadores aritméticos: + (adição), - (subtração), * (multiplicação), % (módulo ou resto de uma divisão), / (divisão);
--operadores lógicos: < (menor que), > (maior que), <= (menor e igual que), >= (maior e igual que), ~= (diferente de), == (igual que)

print(2 + 2, "adicao de 2 + 2");
print(2 - 2, "subtracao de 2 - 2");
print(2 * 2, "multiplicacao de 2 * 2");
print(2 / 2, "divisao de 2 / 2");

print("-----------------------------------------------------------")

print(2 < 1, "2 e menor que 1?");
print(2 > 1, "2 e maior que 1?");
print(2 <= 1, "2 e menor ou igual a 1?");
print(2 >= 1, "2 e maior ou igual a 1?");
print(2 ~= 1, "2 e diferente de 1?");
print(2 == 1, "2 e igual de 1?");